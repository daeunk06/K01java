package ex08class;

public class TestAutoClass {
	 String carName;
	 int carYrear;
	 String owner;
	 
	public TestAutoClass(String carName, int carYrear, String owner) {
		super();
		this.carName = carName;
		this.carYrear = carYrear;
		this.owner = owner;
	}

	public String getCarName() {
		return carName;
	}

	public void setCarName(String carName) {
		this.carName = carName;
	}

	public int getCarYrear() {
		return carYrear;
	}

	public void setCarYrear(int carYrear) {
		this.carYrear = carYrear;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}
	 
	
	
	
}

























